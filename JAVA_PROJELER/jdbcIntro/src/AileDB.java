public class AileDB {
    private String id;
    private String name;
    private String lastName;
    private String age;
    private String student;
    private String worker;

    public AileDB(String id, String name, String lastName, String age, String student, String worker) {
        //Constructor for AileDB
        this.id = id;
        this.name = name;
        this.lastName = lastName;
        this.age = age;
        this.student = student;
        this.worker = worker;
    }

}
